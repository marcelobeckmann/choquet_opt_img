	public static String gerarNomePessoa()
	{
		StringBuffer nome= new StringBuffer();
		for (int i=0;i<4;i++)
		{
			int numero= (int)(Math.random()*nomes.size());
			nome.append(nomes.get(numero)+" ");
			
		}
		return nome.toString().trim();
		
	}

	public static Date gerarDataNascimento()
	{
		
		int dia= (int)(Math.random()*28+1);
		int mes=(int)(Math.random()*12+1);
		int ano=(int)(Math.random()*60+1);
		ano+=1940;
		Calendar cal = new GregorianCalendar();
		cal.set(ano,mes,dia);
		return cal.getTime();
		
	}

	public static double gerarValor()
	{
		String valor =String.valueOf(Math.random()*100000); 
				
		return Double.parseDouble(valor.substring(0, valor.indexOf('.')+3));
		
	}

	public static String gerarNumeroString(int largura)
	{
		return String.valueOf(Math.random()).replace('.','2').substring(2,largura+2);
		
		
	}

